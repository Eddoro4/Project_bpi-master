Программа предназначена для формирования и ведения отчетов по научно-исследовательской работе (НИР) и учебных отчетов.

2. Основные разделы
- Отчет по НИР / Учебный отчет: содержат подразделы с таблицами и текстовыми полями для заполнения данными.
- Архив: хранит все сформированные итоговые отчеты для скачивания или удаления.
- Шаблоны: позволяет просматривать шаблоны отчетов.
